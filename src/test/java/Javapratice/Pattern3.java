package Javapratice;

public class Pattern3 {
	//decrease with space
		public static void main(String[] args) {
			int row = 5;
			int count = 5;

			for ( int i = 1; i<= row; i++)
			{
			
			for (int k=1; k<= count; k++)
			{
			System.out.print("* ");
			}
			System.out.println();
			count--;

			}
			}
			
		
		}


